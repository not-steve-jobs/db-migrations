import { DeepPartial } from 'typeorm';

import { QuestionnaireGroups } from '../../../entity/questionnaire/QuestionnaireGroups';

export function getQuestionnaireGroups(existingGroups : QuestionnaireGroups[]): DeepPartial<QuestionnaireGroups>[] {
  const questionnaireGroups = seedQuestionnaireGroups.slice();

  if (!existingGroups || existingGroups.length === 0) {
    return questionnaireGroups;
  }

  // Needed because of 1690982832241-add-partners-questionnaire-for-new-knn-authority.ts which requires it
  // Needed because of 1707302414664-add-partners-questionnaire-for-new-cri-authority.ts which requires it
  // Needed because of 1707303499411-add-partners-questionnaire-for-new-lca-authority.ts which requires it
  reuseExistingQuestionnaireGroup('personalDetailsPartners', existingGroups, questionnaireGroups);
  reuseExistingQuestionnaireGroup('termsAndConditionsPartners', existingGroups, questionnaireGroups);

  return questionnaireGroups;
}

function reuseExistingQuestionnaireGroup(groupKey: string, existingGroups: QuestionnaireGroups[], questionnaireGroups : DeepPartial<QuestionnaireGroups>[]): void {
  const existingGroup = existingGroups.find(group => group.groupKey === groupKey);
  if (existingGroup) {
    console.log(`Questionnaire group ${groupKey} already exists, will reuse it.`);
    const groupIndex = questionnaireGroups.findIndex(group => group.groupKey === groupKey);
    questionnaireGroups[groupIndex].id = existingGroup.id;
  }
}

